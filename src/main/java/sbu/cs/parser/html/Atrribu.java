package sbu.cs.parser.html;

class Attribu
{
    private String key;
    private String value;

    public Attribu(String value)
    {
        this.key = key;
        this.value = value;
    }

    public String getKey()
    {
        return key;
    }

    public String getValue()
    {
        return value;
    }
}
